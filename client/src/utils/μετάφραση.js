import { ελληνικάΚείμενα, αγγλικάΚείμενα, σουηδικάΚείμενα } from '../db/κείμεναΜετάφρασης';

const μετάφραση = (κείμενο, γλώσσα) => {
  const ευρετήριοΌρου = ελληνικάΚείμενα.findIndex((ε) => ε === κείμενο);
  const όροςΣεΝέαΓλώσσα =
    γλώσσα === 'english'
      ? αγγλικάΚείμενα[ευρετήριοΌρου]
      : γλώσσα === 'svenska'
      ? σουηδικάΚείμενα[ευρετήριοΌρου]
      : κείμενο;

  return όροςΣεΝέαΓλώσσα;
};

export default μετάφραση ;
